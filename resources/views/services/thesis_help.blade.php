                        <p class="m7">Thesis writing is sometimes confused with dissertation writing. But there is a difference; Thesis writing is the end of master’s degree whereas dissertation writing is required when a student is pursuing his or her doctoral degree. So a student may be asked to write a thesis before his masters or doctoral degree ends. So a student won’t be asked to write a thesis during his college days but he should be aware about the process and this can be done by going through the sample works. Thesis writing helps to judge the knowledge gained by the student and test his analytical skills.
                        </p>
                        
                        <p class="m7"><b>Importance of writing a thesis </b></br>
                        Thesis writing is not only an affirmation of the students doctoral or master’s degree but it stays with the student till the time they are associated with the field mentioned in the thesis. When a thesis has been accepted it boosts the morale of the student and can be used by other students studying in the same field while writing their assignments. There is a possibility that the thesis writing may get published in some international journal article and students may refer to it for guidance.
                        </p>

                        <p class="m7"><b>Structure of thesis writing </b></br>
                        Like any other assignment, the thesis does have a concrete and specific structure. Sometimes it may be a daunting task due to its structure but taking help from thesis writing help services may solve the problem. There is a chronological description of the content but the structure may vary depending upon the university. The structure mentioned in the below diagram is generally followed:
                        <ul>
                            <li><b>Title: </b>The first page of thesis writing is the title page. It helps the reader to know about the topic.</li>
                            </br>
                            <li><b>Abstract: </b>Abstract comes after the title page and it should be written once the thesis writing is complete. It informs the reader about the points which will be covered in the thesis writing. Abstract of the Thesis writing helps the reader to note down the findings and the conclusion. It is always advisable to write the abstract once the entire thesis content has been written so that no point is missed.</li>
                            </br>
                            <li><b>Introduction: </b>The first paragraph of the thesis writing helps the reader to know more about the topic by providing an overview of the topic. It states the reason behind selecting the topic and the methods that will be used to solve the problem raised in the thesis paper. Specifically the thesis writer has to answer Why, how and what related to the topic in the form of why the topic was selected, how it will be studied and what is being done to study the topic. The problem statement is specifically mentioned which will be answered throughout the writing. Problem statements will lead to the development of research questions. The research questions should relate to the topic.</li>
                            </br>
                            <li><b>Literature review: </b>It includes a collection of papers which will be used to prove your point highlighting the gap areas in some of the research done which lead you to develop this thesis paper. The thesis writing will help in highlighting the gap areas as well as provide assistance on how to fill those areas. When using a literature source the writer should analyze its content and write its advantages as well as the drawbacks. Using a wide range of research articles will help in proving the point of view of the writer.</li>
                            </br>
                            <li><b>Methodology: </b>This section will help in solving the problem statement and the questions framed in the introduction part. It will state about the method used while conducting the research like qualitative, quantitative or mixed. It provides an insight about the techniques or the methods which will be used to collect the data, its analysis and the reason behind selecting the method. The writer should mention whether the methods used will be able to justify the claim made in the thesis writing paper or not.</li>
                            </br>
                            <li><b>Data Analysis: </b>This section will have information related to the findings gathered while conducting the research and its analysis. It consists of the facts from the research conducted. Tables or graphs can be used to showcase the results generated from the research. Comparing the results with the literature source used in the thesis writing will help in giving a clear picture about the problem statement.</li>
                            </br>
                            <li><b>Discussion:</b>This section will provide a detailed commentary on the findings explaining the result and its significance. It will state whether the result generated is as per the expectation or not. The limitations of the study will be discussed. This section of the thesis writing helps to show that the writer has understood the topic.</li>
                            </br>
                            <li><b>Conclusion: </b>It will restate the topic and the problem statement. It will highlight the important findings and the results. It can also include some recommendations for future improvements and how the limitations mentioned can be filled. The conclusion will provide an overview of all the sections that the writer has covered in the thesis paper.</li>
                            
                        </ul>
                        </p>

                        <p class="m7"><b>Types of thesis writing </b></br>
                        Like essays, thesis does have certain types and they are different from each other. There are three major thesis writings discussed below:
                        <ul>
                            <li><b>Analytical thesis: </b>This type of thesis is used to analyze a single issue. It is one of the common types of thesis writing being adopted by the students. It provides a detailed explanation of the topic. The student’s need to inform what they are going to analyze in the paper, why they are doing so, how it will be done and what would be the benefits.</li>
                            </br>
                            <li><b>Argumentative thesis: </b>As the name suggests the thesis paper will include arguments. The topic has to be described and the student needs to prove his point of view through different arguments. The arguments will be supported through evidence and the opinion needs to be defended till the end.</li>
                            </br>
                            <li><b>Expository thesis: </b>The main goal of expository thesis writing is to explain the concept and the goal of the topic. It is one of the organized ways of writing a thesis paper and every section should be analyzed with the help of evidence.</li>
                            </br>
                        </ul>
                        </p>
