                        How to write a Business Report
                        </h2><br>
                        <p class="m7">A good business report should be structured in such a way that its clear, concise and easy to understand for the target audience. As the name suggests, business reports are required to provide data followed by required analysis to assist Senior management or Investors or Business teams make informed decisions. </p>
                        
                        <p class="m7"><b>Business Report Structure:
                                 </b> Different organizations may have different requirements but a report can generally be structured as follows. Given the nature of report or business, you can choose to skip one or more sections.</p>
 
                        <p class="m7">
                            <b>Title: </b>
                            Majorly there are three central problems of an economy that every economy faces i.e.
                        </p>
                        <p class="m7">
                            <b>Contents: </b>
                            If the report is lengthy, you should include a table of contents. In this section, list the chapter and corresponding section headings. You should also include a page number with for every row in table of contents.
                        </p>
                        <p class="m7">
                            <b>Executive Summary: </b>
                            It's always a good practice to include an executive summary stating the aim and purpose of report. A good summary can briefly talk about historical developments/background of the report that led to the report, state problems under investigation and explain recommended solutions. It should also disclose limitations and assumptions taken while preparing the report. Generally this is prepared at the end of report writing but placed first for senior management who might not have the time to read the entire report.
                        </p>
                        <p class="m7">
                            <b>Introduction: </b>
                            Here you should provide the background information and describe historical developments in detail that led to this report. This section sets the stage for what can a reader expect in upcoming chapters.
                        </p>
                        <p class="m7">
                            <b>Procedure or method of gathering information: </b>
                            Describe the method of gathering information and its limitations in detail. You can also talk about steps taken to overcome the limitations of method of gathering information.
                        </p>
                        
                        <p class="m7">
                            <b>Findings: </b>
                            You should corroborate your findings with enough data to show that you have carried out the research thoroughly. </br>To justify your recommendations, you may also include opinions of experts of the industry, consumer reports, statistical data and your own observations. Do remember that while the objective of your business report is to provide the best solution to a business problem, you may include analysis of other options available and prove them otherwise. You can also help the reader to point to further readings if it's not possible to include them in your report.
                        </p>
                        <p class="m7"><b>Conclusions: </b>
                        This section lists the major inferences that can be drawn from the findings and propose recommendations. It sums up the assessment of current situation based  on your observations, findings and proposes future plan of action. 

                        </p>
                        
                        <p class="m7">
                            <b>Reference list:  </b>The reference list, in alphabetical order, mentions all resources used in creation of your report. Whenever you use information from other sources such as books, online periodicals, articles that helped you write the report, you should include it in your report.
                        </p>

                        <p class="m7">
                        <b>Appendices: </b>At the end, you can use an appendix to group similar items together. Any important material/supplementary information for your discussion can be included in appendix and referred to from within the report. A single appendix can be titled as APPENDIX. If you have multiple items appended, they can be titles as APPENDIX A, APPENDIX B, etc. Also appendices should appear in the order they are mentioned in the report.
                        </p>
                        
                        
                        <p class="m7">
                        <b>Following things should be kept in mind while writing the report:</b>
                        <ol>
                           <li>Grammar, spellings and facts should be accurate.</li>
                           <li>Make sure to use a consistent voice (Active or passive).</li>
                           <li>Every college/organizations may have its own report format and its always better to use the format suggested by your organization.</li>
                        </ol>
